		nähdä huippuja läheltä ja päästä vähän sisälle heidän kisastrategioistaan. Ilma säkenöi
		jännitystä, kun tanssijat lämmittelevät kisoja varten. Kisaajat näyttävät hermostuneilta, mutta
		näytön paikan tullen kaikki stressi katoaa.
	</p>
	<p>
		Itse tanssi kestää puolitoista minuuttia. Tanssiessa se tuntuu pitkältä, mutta sen loputtua,
		aika tuntuikin menevän liian nopeasti. Suorituksen jälkeen voi ottaa rennosti ja ihailla muiden
		suorituksia.
	</p>
