	<h1 id="kisaaminen">Kisaaminen</h1>
	<p>
		Lavatanssien suurin ja hienoin anti on sen sosiaalisuus. Sosiaaliset tanssit ovat hyviä tapoja
		tutustua uusiin ihmisiin. Tärkeintä on, että fiilis on hyvä ja kummallakin on hauskaa.
	</p>
	<p>Silla ei ole siis niin väliä, miltä tanssi oikeasti näyttää, kunhan fiilis on oikea.</p>
	<p>
		Kilpatanssissa on vähän erilaisempi tavoite kuin lavatansseissa. Ei saa vain tuntua hyvältä,
		vaan sen pitää myös näyttää hyvältä. Treenatakin pitää välillä, vaikka ei jaksaisi tai
		huvittaisi.
	</p>
	<p>Eli tanssitaan muille ja näytetään todella hienoilta, vaikka se ei aina tunnukaan hyvältä.</p>
	<p>
		Esimerkit ovat tietenkin vedetty ääripäistä, mutta ne voivat olla suuntaa antavia. Osa on sitä
		mieltä, että tanssia pitäisi vain, koska se on hauskaa. Jotkut taas haluavat tavoitteita, joihin
		liittyy vertailtavuus ja paremmuus. Joillekin tanssiminen on subjektiivista; tanssijoita ei
		pysty tai saa laittaa paremmuusjärjestykseen. Jokaisen suhde tanssiin on kuitenkin
		henkilökohtainen. Tässä on siis minun näkökulmani kisatanssista.
	</p>
	<p>Spoileri: kisaaminen on mahtavaa ja jättää aina hienon tunteen.</p>
	<h2 id="kisatanssiin-liittyvät-säännöt">Kisatanssiin liittyvät säännöt</h2>
	<p>
		Kisaaminen on yksi parhaimmista tavoista saada lisää motivaatiota tanssin oppimiseen. Sen
		yhteydessä tanssin perusteet ja sen säännöt on välttämättömiä osata kunnolla. Lavatansseissa
		sääntöjä saa rikkoa ja välillä jopa pitääkin, jotta tanssiin saadaan lisää maustetta ja
		persoonallisuutta. Kisoissa säännöistä on kuitenkin pidettävä kiinni, tai joutuu sijoituksissa
		jonon hännille.
	</p>
	<p>
		Säännöt tuntuvat aluksi luovan rajoitteita, mutta tanssin oppimisen myötä ne oikeastaan antavat
		omanlaisensa vapauden. Sääntöjen rajoissa tanssiparin kanssa syntyy aivan uudentasoinen
		luottamus ja yhteys, eikä näin ollen nopeassakaan temmossa tule yhteentörmäyksiä.
	</p>
	<p>
		Lähinnä säännöt ovat sitä varten, että tanssilaji näyttäisi sellaiselta kuin sen pitäisi
		näyttää. Muutenhan kisoissa voisi tanssia väärää lajia.
	</p>
	<p>
		Suomen tanssiurheiluliitto on koostanut <a
			href="]https://www.dancesport.fi/toiminta/kilpailutoiminta/kilpailusaannot-ja-ohjeet/"
			>säännöt omaksi sivukseen</a
		>. Näistä kannattaa ainakin vilkaista Rock’n’swing tanssisäännöt.
	</p>
	<h2 id="hyviä-syitä-kisata">Hyviä syitä kisata</h2>
	<p>
		Kisaamista ei tarvitse ottaa liian vakavasti. Kisoissa tanssilajien eri taitotasoista on
		mahdollista valita mieleisensä ja osallistua sen mukaan, kuinka paljon on valmis viettämään
		aikaa tanssin harjoitteluun. Käytännössä POT:n ja HOT:n jatkotason (viimeisin taso) kurssit
		antavat riittävät taidot kisaamiseen lähes minkä tahansa tanssilajin alimmassa
		taitoluokkatasossa.
	</p>
	<p>
		Kisaaminen on loistava yhteisharrastus, sillä harjoittelukertojen lisääntyminen antaa arvokasta
		aikaa toisen kanssa ja yhdessä kehittyminen tuo ainutlaatuisen onnistumisen tunteen. Tekniikan
		lisäksi yhteinen musiikintulkintakin kehittyy tasolle, jossa se on kuin ajatusten lukemista.
	</p>
	<p>
		Yleensä kisataan saman parin kanssa, mutta on myös olemassa kisoja, joissa pari arvotaan. Jos
		erityisesti tykkää tutustua uusiin ihmisiin tanssin kautta, ja haluaisi sen tapahtuvan myös
		kisoissa, on West Coast Swing hyvä laji siihen. Kisoissa viejälle arvotaan seuraaja, joka saa
		päättää soitettavan musiikin.
	</p>
	<h2 id="kisoihin-osallistuminen">Kisoihin osallistuminen</h2>
	<p>
		Jos suoraan virallisiin kisoihin osallistuminen pelottaa, kannattaa ensin osallistua
		satunnaisesti järjestettäviin kevyihin kisoihin, joihin voi siis osallistua ilman
		tanssijärjestön kisalisenssiä ja vakuutusta. Esimerkiksi Comets tanssiseura ja Avec tanssikerho
		järjestävät joskus näitä epävirallisia kisoja. POT ja HOT eivät kisoja yleensä pidä, koska
		kerhot keskittyvät lavatansseihin.
	</p>
	<p>
		Epävirallisissa kisoissa usein arvotaan satunnainen tanssipari, joten jos oman parin kanssa
		kisaaminen kiinnostaa enemmän, virallisiin kisoihin osallistuminen voi olla ainoa vaihtoehto.
	</p>
	<p>
		Virallisiin kisoihin voi osallistua vain seurojen kautta. Helsingissä toimii esimerkiksi Comets,
		jonka kisailtavat lajit ovat mm. Boogie Woogie, Bugg, Rockabilly, Lindy Hop, Fusku, West Coast
		Swing jne. Käytännössä POT:ia ja HOT:ia ei voi edustaa kisoissa, vaikka yllättävän iso osa
		kisoihin osallistuneista nuorista onkin osakuntien tanssikerhoista.
	</p>
	<p>
		Comets keskittyy lähinnä Rock’n’Roll Swing tyylisiin lajeihin. Jos haluaa kisata suljetun otteen
		lajeissa, kuten esim. Valssissa tai Foksissa, on näihin kisoihin mentävä muun seuran kautta kuin
		Cometsin. Sama pätee latinalaisissa tansseissa.
	</p>
	<p>
		POTtilaisten ja HOTtilaisten kannattanee keskittyä Cometsin kautta kilpailevista lajeista
		Fuskuun ja Buggiin, sillä kyseisten tanssilajien opetusta annetaan kaikista eniten osakunnan
		tanssikerhoissa. Alin taitotasoluokka ei myöskään ole murskaavan kova verrattuna esimerkiksi
		Boogie Woogiehin ja West Coast Swingiin. Lähinnä tämä johtuu vain siitä, että näitä lajeja ei
		opetata kovin paljon POT:lla ja HOT:lla. BW ja WCS esiintyvät yleensä vain erilliskursseina ja
		satunnaisesti kausikursseina.
	</p>
	<p>
		Kisamielessä myös BW ja WCS ovat omalla tavallaan korkeammalla kuin Bugg ja Fusku, koska ne ovat
		näytösmäisempiä tanssilajeja. Tuomaristo on lavan yhdellä puolella arvostelemassa yleensä joko
		yhden tai kahden tanssiparin tanssia, joten kaikki tanssillinen suoritus tulee näkyä siinä
		suunnassa. Buggissa sen sijaan tuomaristo levittäytyy ympäri tanssilattiaa, ja tanssin on
		näytettävä hyvältä joka suuntaan. Buggin ja Fuskun tanssilavamaisempi tunnelma johtuu myös
		siitä, että kisan aikana tanssilattialla on monta tanssiparia samaan aikaan.
	</p>
	<p>
		Kisaamista varten on siis liityttävä Cometsin jäseneksi (kirjoittamishetkellä 25 €) ja ostettava
		kisalisenssi sekä -vakuutus, jotka Comets maksaa takaisin korvaushakemusten täyttäneille.
		Jäsenyydet, lisenssit ja vakuutukset ovat voimassa kalenterivuoden ajan. Comets maksaa myös
		kaikki osallistumismaksut kisoihin, joten käytännössä kisaaminen siis maksaa 25 € (Comets
		jäsenyys) ja siihen liittyvät matkustuskustannukset.
	</p>
	<p>
		Lisää voi lukea Cometsin <a
			href="https://www.comets.fi/osallistu-kilpailuihin/rock-ja-swing-tanssikilpailut/uuden-kilpailijan-ohjeet/"
			>uuden kilpailijan ohjeista</a
		>.
	</p>
	<p>
		Kisojen lähestyessä kisailmoittautumiset aukavat. Nämä ilmoitetaan Cometsin omassa
		facebook-kisaryhmässä. Ilmoittautuminen tehdään Cometsin kisavastaavalle, joka ilmoittaa
		osallistumisesi kisajärjestäjälle.
	</p>
	<p>
		Comets tarjoaa jäsenilleen ilmaiseksi salivuoroja harjoittelua varten. Myös HOT on antanut
		osakuntalaisilleen mahdollisuuden harjoitella saleissaan ilmaiseksi. Koska POT on kolmen
		osakunnan yhteinen tanssikerho, kannattaa kysyä omalta osakunnalta, onko saleissa mahdollista
		tanssia.
	</p>
	<h2 id="kisoihin-harjoitelu">Kisoihin harjoitelu</h2>
	<p>
		Kannattaa muistaa, että POT:lla ja HOT:lla opetetaan sosiaalisia tansseja. Tavoitteena on, että
		tanssiosaaminen olisi lajien kohdalla monipuolista, ja että pystyisi tanssimaan jokaiseen
		musiikkiin mitä lavoilla soitetaan. Puolen vuoden aikana opetetaan kaksi kertaa jotakin lajia,
		joita on n. kuusi erilaista. Esimerkiksi Buggia opetetaan yhteensä kauden aikana vain kolme
		tuntia.
	</p>
	<p>
		Comets opettaa yhtä lajia kerralla, jolloin Buggia tulee treenattua kauden aikana n.
		kuusinkertainen määrä siihen, mitä POT:lla ja HOT:lla opetetaan.
	</p>
	<p>
		Alimmalla taitotasolla kyllä pärjää, vaikka ei kävisikään Cometsilla harjoittelemassa. Kaikista
		tärkeintä on se, miten paljon aikoo harjoitella kurssien ulkopuolella. Myöskin odotusten
		kannattaa olla realistisia. Harjoittelukerrat määrittävät sen, kuinka korkealle taitotasolle
		kannattaa tähdätä.
	</p>
	<p>
		Esilletuloa ja lopetusta kannattaa harjoitella erikseen. Tätä ei yleensä missään muualla paitsi
		kisaryhmän oppitunneilla harjoitella. Esilletulo on yllättävän haastavaa. Se kannattaa videoida
		ja harjoitella kunnolla eri kuvakulmien kautta, koska vain siten pystyy näkemään, miltä se
		todella näyttää. Hyvin suunniteltu ja toteutettu esilletulo antaa tuomareille sekä yleisölle
		hyvän ensivaikutelman. Se on myös melko sovittu koreografia, joten sopikaa partnerin kanssa,
		miten se tapahtuu.
	</p>
	<p>
		Myös lopetus on tehtävä tyylikkäästi. Usein lopetuksen vaarana on, että lopettaa liian aikaisin.
		Musiikki yleensä hiljennetään pikkuhiljaa, mutta tanssia voi periaatteessa jatkaa musiikin
		ohikin. Liian nopeasti lopetettu tanssi vaikuttaa hätiköidyltä ja huolimattomalta. On parempi
		lopettaa liian myöhään kuin liian aikaisin. Lopussakin kannattaa tehdä jonkinlainen kumarrus ja
		kiitos yleisöä päin.
	</p>
	<p>
		Vaikka tanssisalissa olisikin peilit, on suositeltavaa kuvata harjoitukset videolle. Se antaa
		realistisemman kuvan omasta tanssitaidostaan ja mahdollisuuden oman tanssin arvostelemiseen ja
		parantamiseen.
	</p>
	<p>
		Monessa lajissa alimmalla taitotasolla on ainoastaan yksi osuus: nopea osuus. Ei siis ihan heti
		tarvitse harjoitella hidasta osuutta. Jos on suunnitelmana pysyä alimmassa taitotasossa, ei
		hidasta osuutta tarvitse lainkaan harjoitella.
	</p>
	<p>Osakuntien tanssikerhoissa hidasta versiota tanssilajeista ei opeteta.</p>
