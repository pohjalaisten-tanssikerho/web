<h2 id="cha-cha">Cha cha</h2>
<p>
	Cha cha on nelijakoinen latinotanssilaji, jota tanssitaan sekä suljetussa tanssiotteessa ilman
	vartalokontaktia että avoimessa tanssiotteessa. Rumban tavoin cha chan tyyli on venyvä, mutta
	iloisen napakka. Cha cha -musiikki yhdessä keinuttavin askelin tempaisevat mukaansa tanssimaan. Se
	on salsan tapaan melko yleinen tanssilaji lavoilla.
</p>
