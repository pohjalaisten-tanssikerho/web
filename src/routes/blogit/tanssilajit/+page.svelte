<h2 id="fusku">Fusku</h2>
<p>
	Hauska ja rytmikäs fusku on rock’n’swing -tansseihin kuuluva tanssilaji ja erittäin suosittu
	lavatanssipiireissä. Sitä tanssitaan enemmän avoimessa otteessa, mutta on mahdollista tanssia myös
	suljetussa otteessa ilman vartalokontaktia. Nelitahtisena tanssilajina sitä on oikeastaan
	mahdollista tanssia lähes kaikkiin nelijakoisiin kappaleisiin. Tahdikuutta tanssiin tuo tuttu
	foksin perusaskel, jossa polvet joustavat joka askeleella.
