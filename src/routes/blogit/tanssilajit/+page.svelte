<h2 id="bugg">Bugg</h2>
<p>
	Ruotsista kotoisin oleva bugg on rock’n’swing-tanssi, jota tanssitaan avoimessa otteessa ja
	kehällä, mutta mahdollisesti myös etenevänä ja linjana. Suosittua buggia pystyy tanssimaan lähes
	kaikkiin nelijakoisiin kappaleisiin lähes kaikilla nopeuksilla. Buggin suosio saattaa perustua sen
	yksinkertaiseen perusaskeleeseen sekä seuraajan että viejän vapauteen tulkita musiikkia ja luoda
	tanssista mieleisensä. Bugg löytyy aika varmasti joka lavatanssin tanssilistalta ja
	kilpatanssimaailmasta varsinkin Suomessa ja Ruotsissa.
</p>

