<h1 id="tanssilajit">Tanssilajit</h1>
<h2 id="bachata">Bachata</h2>
<p>
	Bachata on usein latinotansseihin yhdistetty nelitahtinen tanssilaji, joka on saanut vaikutteita
	muista tansseista kuten bolerosta. Bachata-musiikkiin tanssittavaa lajia pystyy tanssimaan sekä
	avoimessa että suljetussa tanssiotteessa vartalo kontaktilla tai ilman. Tanssille ominaista on
	varsinkin seuraajan keinuva liike ja ylävartalosta viedyt vartalon rotaatiot. Viemisen vahvasta
	roolista huolimatta tanssi tarjoaa monta hyvää kohtaa koristella tanssia ja nauttia siitä.
	Perusaskeleet ovat yksinkertaiset sivulta sivulle kulkevat, joissa yleensä neljäs askel on täppi
	tai muu vastaava. Lavoilla bachata on vielä aika harvinainen, mutta sen suosio nousee koko ajan
	räjähdysmäisesti.
</p>
