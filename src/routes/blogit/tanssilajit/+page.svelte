<h2 id="salsa">Salsa</h2>
<p>
	Salsa on latinotansseista ehkä jopa suosituin tanssilaji, jota näkee niin lavoilla ympäri Suomea
	kuin muuallakin maailmassa. Se on nelijakoinen tanssi, jonka musiikin rytmikuvioiden
	päällekkäisyys ja reipas tahti vie nopeasti mukanaan. Sitä tanssitaan suljetussa tanssiotteessa
	ilman vartalokontaktia, mutta usein suurin osa tanssista kuitenkin tapahtuu avoimessa
	tanssiotteessa monien kuvioiden kera.
</p>
