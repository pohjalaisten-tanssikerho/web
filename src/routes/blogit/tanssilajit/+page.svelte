<h2 id="kizomba">Kizomba</h2>
<p>
	Kizomba on lähitanssilajiksi erittäin monipuolinen. Sille ominaista on vartalokontaktin lisäksi
	erilaiset askelvariaatiot ja yläkropasta tapahtuva vienti sekä seuraaminen. Voimakkaaseen ja
	tunnistettavaan kizomba-musiikkiin yhdistettynä tanssi luo rauhallisen ja intensiivisen tunnelman.
	Tanssilavoilla sen suosio on kasvanut hurjaa vauhtia, mutta siltikään joka lavalla kizombaa ei
	tanssita.
</p>
