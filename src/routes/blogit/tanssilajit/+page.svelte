<h2 id="boogie-woogie">Boogie woogie</h2>
<p>
	50-luvulta peräisin oleva boogie woogie on avoimen otteen rock’n’swing-tanssilaji, jota tanssitaan
	rock’n’roll- musiikkiin. Tanssin ominaispiirteisiin kuuluvat muun muassa musiikkiin eläytyminen,
	hassuttelu ja akrobaattiset liikkeet, jotka luovat tanssiin hauskan ja pirteän tunnelman. Sitä
	tanssitaan linjassa ja sen perusaskeleet koostuvat kahdeksan askeleen kokonaisuuksista, jotka
	nopeimmilla lajin tanssijoilla saavat näyttämään aivan kuin he leijuisivat ilmassa. Tanssi on
	vähän harvinaisempi lavoilla, mutta erityisen suosittu kilpatanssimaailmassa.
</p>
