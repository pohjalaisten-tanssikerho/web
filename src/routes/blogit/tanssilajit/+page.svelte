<h2 id="rumba-bolero">Rumba bolero</h2>
<p>
	Rumba on Kuubasta kotoisin oleva nelijakoinen latinalaistanssilaji, jota tahdittaa
	bolero-rytmilajiin kuuluva musiikki. Sitä tanssitaan usein suljetussa tanssiotteessa ilman
	vartalokontaktia, mutta voidaan tanssia myös avoimessa tanssiotteessa. Tunnelmaltaan se on
	rauhallinen ja intensiivisen hidas, joka näkyy myös tanssille ominaisena venyvänä kehon
	liikehdintänä. Lavoilla rumbaa tanssitaan vain vähän.
</p>
