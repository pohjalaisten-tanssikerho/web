<h2 id="valssi">Valssi</h2>
<p>
	Valssi on lavoilla tanssittavista tanssilajeista vanhin, ja on tuttu esimerkiksi häätanssina.
	Kolmijakoisena tanssina se on erittäin hienostunut ja ylväs lähitanssi ilman vartalokontaktia.
	Valssi aloitti paritanssin historian, jonka vuoksi perinteiden mukaisesti valssi aloittaa sekä
	lopettaa tanssiaiset. Sitä säestää <sup>3</sup>⁄<sub>4</sub> jakoinen musiikki, jossa ykkösisku on
	korostettuna. Valssin keinuvaan ja etenevän pyörivään huumaan pääsee helposti kiinni.
</p>
