<h2 id="tango">Tango</h2>
<p>
	Suomalainen tango on nelijakoinen, erittäin suosittu lavatanssilaji, jota näkee oikeastaan
	kaikilla lavoilla ympäri Suomea. Tyyliltään se on etenevä ja hienostunut tanssi, jota nimensä
	mukaisesti tanssitaan tango-musiikkiin. Musiikin aiheina ovat yleensä rakkaus, tuska tai onni
	korostamassa tanssin tunnelmaa. Sitä tanssitaan suljetussa tanssiotteessa, mutta esimerkiksi
	promenadissa ja joidenkin muiden kuvioiden kohdalla tanssiote irrottautuu. Myös argentiinalainen
	tango on herättänyt suosiota lavoilla. Normaalista tangosta se eroaa varsinkin jalkavariaatioiden
	suhteen, sillä argentiinalaisessa tangossa niitä ei ole tarkoin määritelty.
</p>
