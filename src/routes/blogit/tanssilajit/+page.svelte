<h2 id="samba">Samba</h2>
<p>
	Samba on kaksijakoinen latinotanssilaji, jota tanssitaan usein suljetussa tanssiotteessa ilman
	vartalokontaktia. Sen perusaskeleet seuraavat rytmiä “yks-a-kaks, yks-a-kaks” ponnauttaen painon
	jalalta toiselle ja aiheuttaen tanssille ominaisen keinuvan liikkeen. Se on reipas ja joustava
	paritanssi. Lavoilla samba ei ole enää viime vuosina ollut kovin yleinen tanssilaji kun muut
	lattarit, kuten salsa, ovat yleistyneet. Kilpatanssimaailmassa sambaa kuitenkin tanssitaan
	enemmän. Lisäksi samban harjoittelusta on paljon hyötyä monia muita tanssilajeja ajatellen!
</p>
