<h2 id="foksi">Foksi</h2>
<p>
	Foksi saattaa olla jopa yleisin lavatanssilaji, koska sitä pystyy soveltamaan ja tanssimaan lähes
	kaikkiin kappaleisiin. Se on nelijakoinen, kuten monet muutkin tanssilajit, ja sitä tanssitaan
	suljetussa tanssiotteessa. Foksi antaa tyylillisesti paljon tilaa musiikista nauttimiseen ja sen
	tulkintaan, ja siksi se tempaiseekin mukaansa tanssijan kuin tanssijan.
</p>
