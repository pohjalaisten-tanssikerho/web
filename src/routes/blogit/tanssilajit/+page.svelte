<h2 id="jive">Jive</h2>
<p>
	Jive on hieman fuskua ja buggia muistuttava nelijakoinen paritanssi, joka kuitenkin kuuluu
	latinalaistanssien perheeseen. Tyyliltään se on nopeatempoinen ja iloinen tanssi, jonka
	perustekniikka ja oikeaoppinen kehon liikehdintä tekee vähän haastavaksi paritanssiksi. Sitä
	tanssitaan nopeatempoiseen swing-musiikkiin. Jive on lavoilla vähän harvinaisempi, mutta
	kilpatanssimaailmassa laji elää vielä vahvasti.
</p>
