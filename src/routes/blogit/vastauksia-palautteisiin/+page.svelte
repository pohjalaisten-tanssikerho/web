<h1 id="vastauksia-palautteisiin">Vastauksia palautteisiin</h1>
<p>
	Saamme kauden päätteeksi kiitettävän määrän palautetta. Näistä on aina mukava suunnitella
	seuraavaa kautta. Hallitus haluaisi joihinkin palautteisiin antaa vastauksia, sillä palautteista
	on osa toistuvia. On olemassa toiveita, joita tanssikerhon hallitus ei voi yksinkertaisesti
	toteuttaa.
</p>
<p>
	Ihanimmat palautteet liittyvät siihen, että halutaan lisää kursseja. Pohjalaisten tanssikerholla
	on kuitenkin rajalliset resurssit, vaikka intoa ja halukkuutta hallituksella onkin. Monesti
	toistuvia palautteita ovat esimerkiksi, että halutaan lisää tiiviskursseja ja tanssibileitä. On
	myöskin toivottu, että saadan jatkotason viikottaisia kursseja myös niille, jotka eivät halua
	vaihtaa pareja. Näitä palautteita yhdistää tanssitilan tarve, mutta Pohjalaisten osakuntien
	yhteisessä käytössä olevat salit ovat eritäin haluttuja ja aktiivisessa käytössä. Saleja käyttävät
	myös PPO, EPO ja VN, sekä muut PV:n alaisuudessa olevat kerhot.
</p>
<p>
	Olemme pyrkineet ratkaisemaan tila-ongelman mm. järjestämällä tanssibileitä ja kursseja välillä
	jossain muualla kuin Bottalla. Näissä on kuitenkin opiskelija- ja osakuntalähtöisyyttä vaikeampi
	pitää, puhumattakaan opiskelijahinnoista.
</p>
<p>
	Kannattaa pitää mielessä, että Bottan salit ovat ensisijaisesti tarkoitettu Pohjalaisten
	osakuntien (EPO, PPO ja VN) jäsenille. Voi olla myös, että jos tanssikerhon jäsenten
	osakuntalaisten osuus kasvaisi, saattaisi PV (Pohjalaisten Valtuuskunta) myöntää tanssikerholle
	lisää varauksia saleilleen. <strong
		>Tanssikerho suositteleekin jokaista jäsentään liittymään osakuntaan.</strong
	> Vaikka et olisikaan opiskelija, voit silti liittyä kannatusjäsenenä osakuntaan. Osakuntaan liittyminen
	tuo myös hyviä etuja, joten liittyminen ei ole täysin turhaa.
</p>
<h2 id="vastauksia-2020-syksyn-palautteisiin">Vastauksia 2020 syksyn palautteisiin</h2>
<p>
	Kaiken kaikkiaan palautteet ovat olleet erittäin positiivisia. Koronajärjestelyt ovat saaneet
	positiivista mainintaa, ja yksityistunneista oli kiitelty. Lajitoiveiden enemmistöä:
</p>
<p><em>Latinalaisia tansseja lisää?</em></p>
<p>
	Korona-pandemian vuoksi kurssit jouduttiin keskeyttämään juuri silloin, kun latinalaiset tanssit
	olivat alkamassa. Uskomme kuitenkin kurssien olleen tasapainossa tanssilajien suhteen, mikäli
	kurssit olisi voitu jatkaa loppuun asti. Lisäämme kuitenkin maltillisesti latinalaisten
	tanssilajien määrää tulevalle kaudelle.
</p>
<p><em>Saisiko lindyä ja westiä?</em></p>
<p>
	Kyllä olkaa hyvä! Lindyä ja westiä ollaan lisäämässä tulevalle kaudelle alkeisjatkoon, sillä
	Marianne on näihin tanssilajeihin hyvin perehtynyt. On kuitenkin vielä vähän kysymysmerkkinä, että
	miten ylipäätänsä kannattaa tai voidaan pitää seuraava kausi, sillä korona on vielä riesana.
</p>
<p>
	Paljon muutakin sekalaisia palautteita oli tullut. Pyrimme jokaisesta palautteesta parantamaan
	toimintaamme tai vähintäänkin arvioimaan, onko meillä resursseja toteuttaa toiveita tai
	palautteiden pohjalta tekemiämme suunnitelmia.
</p>
